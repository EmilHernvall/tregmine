	private static final int SANTA_SPAWN_CHANCE = 1; // Out of 997

	public void populate(World world, Random random, Chunk source) {
		if (random.nextInt(997) <= SANTA_SPAWN_CHANCE) {
			int x = (source.getX() << 4) + random.nextInt(16);
			int z = (source.getZ() << 4) + random.nextInt(16);
			int y = world.getHighestBlockYAt(x, z);
			generate(world, random, x - 1 , y-4, z - 1);
		}
	}

	public void getRandom(int lower, int upper, Chest chest) {
		Random random = new Random();
		int rand = random.nextInt((upper - lower) + 1) + lower;

		if(rand == 1){
			setHelmet(chest);
		}else if(rand == 2){
			setChest(chest);
		}else if(rand == 3){
			setLegs(chest);
		}else if(rand == 4){
			setBoots(chest);
		}else{
			setCake(chest);
		}
	}

	void fillChest(Block block) {
		if (block.getState() instanceof Chest) {

			Chest chest = (Chest) block.getState();

			getRandom (1,5,chest);
			chest.update();
		}
	}

